# ✅ 선택한 MBTI에 따라 추천 결과 출력
if selected_mbti:
    profile = mbti_profiles[selected_mbti]

    st.markdown(f"## 💬 당신은... {selected_mbti}!")
    st.markdown(f"**{profile['style']}**")

    for field_name, jobs in profile["fields"].items():
        st.markdown(f"### 🧭 {field_name}")
        for job_title, job_reason in jobs:
            st.markdown(f"- **{job_title}** — {job_reason}")

    st.markdown("---")
    st.success("📌 진로는 정답이 아니라 탐험이에요. 당신의 성향을 나침반 삼아 나만의 길을 만들어보세요!")
